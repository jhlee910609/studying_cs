package algorithm;

public class AllLongestStrings {

    String[] allLongestStrings(String[] inputArray) {

        int maxLength = 0;
        int lengthCount = 0;

        for (int i = 0; i < inputArray.length; i++) {
            if (maxLength < inputArray[i].length()) {
                maxLength = inputArray[i].length();
                lengthCount++;
            }
        }
    }
}
